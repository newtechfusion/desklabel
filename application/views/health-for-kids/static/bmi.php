        <p>BMI stands for Body Mass Index and is a measurement of a person's height and weight in correlation
          with each
          other in
          order to determine body fat. It's important to understand that a BMI measurement isn't an exact
          figure, however
          it
          is a strong indicator of the body fat percentage. This makes it a good tool for accessing a person's
          health in
          relation to body fat. </p>
        <p>Most people are familiar with the adult BMI calculations, but a special Body Mass Index has now been
          created for
          children and teens as well, often times referred to as BMI-for-age. It's only sensible that kids age
          2-19 have
          their
          own scale of measurements because they are continuously growing. This is going to alter the results,
          which are
          dependent upon age and sex of the child in addition to their height and weight. </p>
        <p>Important fact: Obsesity in children has been measured at more than 25% total body fat in boys and
          32% total body fat in girls. </p>
        <p>Though the math involved can get rather complicated, there are now easy to use <a href="http://apps.nccd.cdc.gov/dnpabmi/Calculator.aspx/">BMI Calculators</a>, like this one from the Centers of
          Disease Control that can help you find out a child's BMI. It's best if you have recent height and weight
          measurements from a physician, but if not using your own measurements will still work.<br/>
        </p>
        <p>Once the BMI is calculated it will give you an actual number which is an estimate of body fat
          percentage. This
          number
          is then applied to a BMI-for-age chart which will give you the percentile range the child falls in.
          Again sex of
          the
          child is a factor so there are separate charts for boys and girls. Based on the BMI percentile the
          child will
          belong
          to one of 4 categories:</p>
        <p><span class="style4">Underweight</span>: below 5th percentile<br/>
          <span class="style4">Healthy weight</span>: 5th up to 85th percentile<br/>
          <span class="style4">At Risk of Being  Overweight</span>: 85th up to 95th percentile<br/>
          <span class="style4">Overweight</span>: 95th percentile and over</p>
        <p>It's important to understand that simply using weight isn't a correct indicator of body fat among
          children. Their
          bodies are growing too rapidly and healthy weight is dependent upon many more factors than that one
          number.
          Additionally, body fat changes with age and there is a significant difference in body fat between
          girls and
          boys.
          However, as mentioned earlier BMI itself isn't an exact science but it is a strong indicator which
          makes it more
          accurate than using just weight to measure body fat. </p>
        <p>If your child's BMI suggests that they are underweight, overweight or at risk to become overweight
          consult with
          your
          physician to discuss it further. When your child does have a physical ask your child's doctor to
          calculate their
          BMI
          as well and get their professional opinion. Dependent upon what they find further tests can be done
          to more
          accurately measure body fat and health related issues. They can analyze factors like diet and family
          history and
          make suggestions for changes based on your child's needs. </p>

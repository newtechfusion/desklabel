<p>A class depends on its teacher to guide them and instruct  them on subjects that they will need in the future for a productive  adulthood.&nbsp; The subject of their health  is one of the most important subjects they must learn but one that is  continually overlooked by the education system.&nbsp;  This problem is magnified by the fact that the health of our youth is  suffering from a poor diet and little exercise.<br>
As a facilitator of learning teachers  have the tools, facilities and means of contact to be a positive role model  that can inform students of the importance of living healthy.&nbsp; It's especially true if your school offers  limited or no health education resources.&nbsp;  Health education can be incorporated into various lessons throughout the  day or be independently taught.<br>
One way you can influence your  class to start getting more active is by focusing on their playground activities.&nbsp; By approaching the subject from various  angles and using an activity that's perceived as fun to children you can teach  them how to make the best use of their recess time.</p>
		        <p><span class="style3">Have a Lesson Plan</span><br>
		          Block out a time to add in a lesson on safe, healthy  playground activities and why students should use this time to get in the daily  exercise their bodies need.&nbsp; Explain to  them the nutrition and physical activity that is required for a healthy  lifestyle and why it's so important.
		          By making them aware that the  choices they make now will affect them well into the future kids will better  understand they are the biggest part of making the change.&nbsp; Go over what types of foods children should  be incorporating into their diet, how they fuel the body, how the body works,  how exercise effects the body, the benefits of exercise and other related  issues that are appropriate.<br>

				<p>It is now known that like adults, childhood obesity and  overweight issues are on the rise.&nbsp; It's  estimated that 1 in every 3 children are overweight or obese which is more than  double what it was 30 years ago.&nbsp; Clearly,  we have a problem here in the U.S. that must be addressed before our children  are put in any more risk.<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Many  children who are overweight carry that into adulthood making it increasingly  more difficult to overcome.&nbsp; As they  advance in age the likelihood that they will remain overweight or obese as  adults goes from roughly 30% in preschool to 70-80% in the teen years.&nbsp; That's why developing healthy nutrition  habits when they are young is so important.&nbsp;  It's your chance to help them develop smart habits in their nutrition choices  before they're on their own and the choices are solely up to them.<br>
